        <h2>Instructions du Jeu de Mémoire (Joueur Unique)</h2>
        <h3>Préparation du Jeu :</h3>
        <p>Les cartes seront soigneusement mélangées et disposées sur la table face cachée.</p>
        
        <h3>Tour du Joueur :</h3>
        <p>Le joueur peut sélectionner deux cartes en les cliquant avec la souris.</p>
        
        <h3>Découverte des Cartes :</h3>
        <p>Si les deux cartes sélectionnées sont identiques, elles resteront découvertes, permettant au joueur de continuer.</p>
        <p>Si les cartes ne correspondent pas, elles seront à nouveau cachées après un court instant.</p>
        
        <h3>Fin du Jeu :</h3>
        <p>Le jeu se poursuit jusqu'à ce que toutes les paires de cartes soient trouvées et découvertes.</p>
        
        <h3>Score et Évaluation :</h3>
        <p>Le score final du joueur est influencé par le nombre d'erreurs commises pendant le jeu.</p>
        <p>Moins il y a d'erreurs, meilleur sera le score du joueur.</p>

        <p>
        Si vous faites moins de 7 erreurs, vous obtenez 3 étoiles. Si vous faites moins de 10 erreurs, vous obtenez 2 étoiles. Si vous faites plus de 13 erreurs, vous n'obtenez aucune étoile et vous avez perdu.
        </p>
